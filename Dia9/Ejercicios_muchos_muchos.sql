# 2. El alumno con mas cursos matriculados
-- El metodo concat_ws funciona de la sgte manera
-- minimo 3 parametros como minimo en el cual el 1er parametro sera para indicar cual es el separador entre
-- columna y columna, y luego los demas parametros sirven para concatenar
select concat_ws(' ',matricula, nombre, apellido) as 'ALUMNO', count(*) 'CURSOS MATRICULADOS'
from alumnos as a
inner join alumnos_cursos as ac
on a.id = ac.alumnos_id
group by concat_ws(' ',matricula, nombre, apellido)
having count(*) = (
	select count(*)
    from alumnos as a
	inner join alumnos_cursos as ac
	on a.id = ac.alumnos_id
    group by nombre, apellido
    order by 1 desc
    limit 1
) -- el having se usa para filtro pero con funciones de agregacion, el having tbn se puede usar como un where
order by count(*) desc;

